/*
    História e Conceitos

    "Uma biblioteca JavaScript para criar interface de usuário" - Reactjs

    Criado em 2011 por Jordan Walke no Facebook.

    Baseado em XHP, um framework para criação de HTML no PHP.

    Utilizado no mural de notícias do Facebook.

    Estavam tendo problema de manutenção e escalabilidade do código.   
    
    2012 - Utilizado no Instagram
    2013 - Anúncio para liberação OpenSource na JSConfUS
    2015 - React Native
    2015 - UWP (Universal Windowns Plataform)

    React é uma biblioteca e não um framework.

    A principal função é a criação da interface para o usuário.

    React é uma linguagem declarativa. Ela não está preocupada com o passos que a aplicação tomou para chegar aos fins. Ou seja, não quer saber se tem uma condição que esta sendo satisfeita para passar pro próximo passo. Ou existe ou não existe.
    Está preocupado apenas com o que é exibido na interface do usuário.

    Pode ser utlizado em qualquer lugar.

    E é baseado em componentes.

    */